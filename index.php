<p>Welcome to the Dota/HoN converter. You can use this tool to find out which heroes in the game are equivalent.</p>
<p>To find corresponding heroes of each game, go to the tab of the game you are playing, and hover over the hero's icon. Alternatively, you can type an item or hero name, and it will give the other game's equivalent, if there is any.</p>
<p>Not all heroes are direct copies of one another. They are usually some hybrid of two heroes. These will <b>not</b> be given as equivalents, however.</p>
<p>The last time this site was updated was December 2014. So any heroes or items that came after that date are not included.</p>
<p>Any suggestions are welcome. Contact me on Reddit or create an issue on the <a href="https://github.com/Dickurt/dhon" target="_blank">GitHub repo</a>.</p>

